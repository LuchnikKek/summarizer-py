import nltk
import numpy as np
import networkx as nx
import re
from sklearn.feature_extraction.text import TfidfVectorizer
from nltk.tokenize import sent_tokenize
from nltk.corpus import stopwords
from transformers import pipeline

nltk.download("punkt")
nltk.download("stopwords")

# Модель для перефразирования (заменяет текст на более плавный пересказ)
rephrase_model = pipeline("text2text-generation", model="IlyaGusev/rut5_base_sum_gazeta")

def clean_text(text):
    """Удаляет лишние пробелы и символы"""
    text = re.sub(r'\s+', ' ', text).strip()
    return text

def summarize_text(text, num_sentences=3):
    # 1. Разбиваем на предложения
    sentences = sent_tokenize(text)
    
    # 2. Вычисляем TF-IDF
    vectorizer = TfidfVectorizer(stop_words=stopwords.words("russian"))
    tfidf_matrix = vectorizer.fit_transform(sentences)
    
    # 3. Строим граф предложений (по косинусному сходству)
    similarity_matrix = (tfidf_matrix * tfidf_matrix.T).toarray()
    np.fill_diagonal(similarity_matrix, 0)  # Обнуляем связь предложения с самим собой
    
    # 4. Применяем TextRank
    graph = nx.from_numpy_array(similarity_matrix)
    scores = nx.pagerank(graph)
    
    # 5. Выбираем топ-N предложений
    ranked_sentences = sorted(((scores[i], s) for i, s in enumerate(sentences)), reverse=True)
    top_sentences = [s for _, s in ranked_sentences[:num_sentences]]
    
    # 6. Перефразируем, чтобы получился связный текст
    summary = " ".join(top_sentences)
    summary = rephrase_model(summary, max_length=200, num_return_sequences=1)[0]["generated_text"]

    return clean_text(summary)


